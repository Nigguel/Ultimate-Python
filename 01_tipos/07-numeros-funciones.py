"""     Funciones nativas para Numeros     """

import math


# Funcion que permite redondear un número
print(round(1.3), round(1.7), round(1.5))

# Funcion que devuelve el valor absoluto de un número
print(abs(-77), abs(55))

# Funcion que devuelve el valor entero mas cercano para arriba
print(math.ceil(1.1))

# Funcion que devuelve el valor entero mas cercano para abajo
print(math.floor(1.999))

# Funcion que permite resolver una potencia
print(math.pow(10, 3))

# Función que permite resolver raiz cuadrada
print(math.sqrt(9))
