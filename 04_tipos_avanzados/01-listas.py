"""     Listas     """

# Listas de Números
NUMEROS = [1, 2, 3]
print(NUMEROS)

# Listas de Letras
LETRAS = ["a", "b", "c"]
print(LETRAS)

# Listas de palabras
PALABRAS = ["Chanchito", "Feliz"]
print(PALABRAS)

# Lista de booleans
BOOLEANS = [True, False, True, True]

# Lista de listas (Matriz)
MATRIZ = [[0, 1], [1, 0], [1, 1]]
print(MATRIZ)

# Multiplicación de listas con numeros enteros
CEROS = [0] * 10
print(CEROS)

# Creando una lista nueva a partir de dos listas
ALFANUMERICO = NUMEROS + LETRAS
print(ALFANUMERICO)

# Creando listas con rangos de numeros
RANGO = list(range(1, 11))
print(RANGO)

# Creando listas con rangos de letras
CHARS = list("hola mundo")
print(CHARS)
